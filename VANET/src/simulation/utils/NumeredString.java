package simulation.utils;

/**
 * model the funtionning of a numered String 
 * @author Jean-Paul Jamont
 */
public class NumeredString {
	/** number associated to the string*/
	public int num;
	/** the string */
	public String string;

	/** default constructor
	 * @param num number associated to the string
	 * @param string the string
	 */
	public NumeredString(int num,String string)
	{
		this.num=num;
		this.string=string;
	}
}
